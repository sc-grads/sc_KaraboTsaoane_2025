# Integer to string
num = 123
num_str = str(num)
print(num_str, type(num_str))  # Output: '123' <class 'str'>

# Float to string
pi = 3.14
pi_str = str(pi)
print(pi_str, type(pi_str))  # Output: '3.14' <class 'str'>

# Boolean to string
flag = True
flag_str = str(flag)
print(flag_str, type(flag_str))  # Output: 'True' <class 'str'>

# List to string
my_list = [1, 2, 3]
list_str = str(my_list)
print(list_str, type(list_str))  # Output: '[1, 2, 3]' <class 'str'>


